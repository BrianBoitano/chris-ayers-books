// Regenerate src/lib/ebooks-data.ts: embed every ENCORE book's EPUB (base64) plus its
// terminal-section set and title, so /api/ebook can serve the free ebook gated by a
// completion token (the EPUBs are NOT served from a static path).
//
// Sources, per book slug found in public/play/<slug>.json:
//   - title + terminal sections: from public/play/<slug>.json
//   - EPUB bytes:                from <ENCORE_REPO>/books/*/volume/<slug>.epub
//
// Run from the site repo root:
//   node scripts/gen-ebooks-data.mjs
//   ENCORE_REPO=/path/to/encore-gamebooks node scripts/gen-ebooks-data.mjs
//
// Re-run whenever a book is added or its EPUB/endings change, then rebuild + deploy.

import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const SITE = join(dirname(fileURLToPath(import.meta.url)), '..');
const ENCORE = process.env.ENCORE_REPO || join(SITE, '..', 'encore-gamebooks');
const PLAY = join(SITE, 'public', 'play');

function findEpub(slug) {
  const booksDir = join(ENCORE, 'books');
  if (!existsSync(booksDir)) return null;
  for (const b of readdirSync(booksDir)) {
    const p = join(booksDir, b, 'volume', `${slug}.epub`);
    if (existsSync(p)) return p;
  }
  return null;
}

const slugs = readdirSync(PLAY)
  .filter((f) => f.endsWith('.json'))
  .map((f) => f.replace(/\.json$/, ''))
  .sort();

const titles = {};
const terminals = {};
const b64 = {};
const missing = [];

for (const slug of slugs) {
  const graph = JSON.parse(readFileSync(join(PLAY, `${slug}.json`), 'utf8'));
  titles[slug] = graph.title || slug;
  terminals[slug] = Object.entries(graph.sections)
    .filter(([, v]) => v.terminal && !v.bridge)
    .map(([n]) => Number(n))
    .sort((a, b) => a - b);
  const epub = findEpub(slug);
  if (!epub) { missing.push(slug); continue; }
  b64[slug] = readFileSync(epub).toString('base64');
}

if (missing.length) {
  console.error(`WARN: no EPUB found for: ${missing.join(', ')} (looked in ${ENCORE}/books/*/volume/). They are skipped.`);
}

const present = slugs.filter((s) => b64[s]);
const out = [];
out.push('// AUTO-GENERATED by scripts/gen-ebooks-data.mjs. Do not edit by hand.');
out.push('// Embedded ENCORE ebooks + per-book terminal-section sets, served gated by');
out.push('// /api/ebook so the EPUBs are not at a guessable static path.');
out.push('// Regenerate when a book is added or its EPUB/endings change.');
out.push('');
out.push('export const TITLES: Record<string,string> = ' + JSON.stringify(Object.fromEntries(present.map((s) => [s, titles[s]]))) + ';');
out.push('');
out.push('// Section numbers that are real book endings (terminal, not a part-bridge).');
out.push('export const TERMINALS: Record<string, number[]> = ' + JSON.stringify(Object.fromEntries(present.map((s) => [s, terminals[s]]))) + ';');
out.push('');
out.push('// base64 of each EPUB (application/epub+zip).');
out.push('export const EBOOKS_B64: Record<string,string> = {');
for (const s of present) out.push(`  ${JSON.stringify(s)}: ${JSON.stringify(b64[s])},`);
out.push('};');

writeFileSync(join(SITE, 'src', 'lib', 'ebooks-data.ts'), out.join('\n') + '\n');
console.log(`wrote src/lib/ebooks-data.ts: ${present.length} books [${present.join(', ')}]`);
